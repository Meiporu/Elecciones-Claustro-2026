/**
 * Retrieves the stored language preference from localStorage or browser default.
 * @returns {string} The language code (es, en, or gl)
 */
function getStoredLanguage() {
  const stored = localStorage.getItem('language');
  if (stored) return stored;

  const browserLang = navigator.language.split('-')[0];
  return ['es', 'en', 'gl'].includes(browserLang) ? browserLang : 'es';
}

let currentLanguage = getStoredLanguage();

/**
 * Translates a key to the current language.
 * @param {string} key - The translation key
 * @returns {string} The translated text or the key if not found
 */
function translate(key) {
  return translations[currentLanguage]?.[key] || translations['es'][key] || key;
}

/**
 * Updates all page elements with data-i18n attributes to display translated content.
 */
function updatePageTranslations() {
  const elements = document.querySelectorAll('[data-i18n]');
  elements.forEach(el => {
    const key = el.getAttribute('data-i18n');
    const translated = translate(key);

    if (el.tagName === 'INPUT' || el.tagName === 'BUTTON') {
      el.placeholder = translated;
      el.value = translated;
    } else {
      el.textContent = translated;
    }
  });

  document.documentElement.lang = currentLanguage;
  localStorage.setItem('language', currentLanguage);
}

/**
 * Switches the application language to the specified language.
 * @param {string} lang - The language code to switch to (es, en, or gl)
 */
function switchLanguage(lang) {
  if (['es', 'en', 'gl'].includes(lang)) {
    currentLanguage = lang;
    updatePageTranslations();
    updateLanguageButtons();
  }
}

/**
 * Updates the visual state of language selector buttons.
 */
function updateLanguageButtons() {
  const buttons = document.querySelectorAll('.lang-btn');
  buttons.forEach(btn => {
    btn.classList.remove('active');
    if (btn.getAttribute('data-lang') === currentLanguage) {
      btn.classList.add('active');
    }
  });
}

document.addEventListener('DOMContentLoaded', () => {
  updatePageTranslations();
  updateLanguageButtons();

  const langButtons = document.querySelectorAll('.lang-btn');
  langButtons.forEach(btn => {
    btn.addEventListener('click', () => {
      switchLanguage(btn.getAttribute('data-lang'));
    });
  });
});
